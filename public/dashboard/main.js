function showMovies(page) {
  const moviesContainer = document.getElementById('moviesContainer');
  const paginationContainer = document.getElementById('pagination');

  const startIndex = (page - 1) * moviesPerPage;
  const endIndex = startIndex + moviesPerPage;

  const currentMovies = movieData.slice(startIndex, endIndex);
  moviesContainer.innerHTML = '';

  currentMovies.forEach(movie => {
    const movieElement = createMovieElement(movie);
    moviesContainer.appendChild(movieElement);
  }
  );

  const totalPages = Math.ceil(movieData.length / moviesPerPage);
  paginationContainer.innerHTML = '';


  const previousButton = document.createElement('button');
  previousButton.textContent = 'Previous';
  previousButton.onclick = () => showMovies(page - 1);
  previousButton.disabled = page === 1;
  paginationContainer.appendChild(previousButton);

  for (let i = 1; i <= totalPages; i++) {
    const button = document.createElement('button');
    button.textContent = i;
    button.onclick = () => showMovies(i);
    paginationContainer.appendChild(button);
  }


  const nextButton = document.createElement('button');
  nextButton.textContent = 'Next';
  nextButton.onclick = () => showMovies(page + 1);
  nextButton.disabled = page === totalPages;
  paginationContainer.appendChild(nextButton);

  updateActiveState(page);
}

